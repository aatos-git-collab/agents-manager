        info "rebranding-tools skill" " not present (optional)"
        HEAL_STATUS+="rebranding-tools        SKIP     optional component\n"
        return
    fi

    # 6a: watchdog exists (may be in scripts/ subdirectory)
    if [ -f "$watchdog" ]; then
        ok "rebranding watchdog" " exists at $watchdog"
    elif [ -f "$rt_skill/scripts/watchdog.py" ]; then
        ok "rebranding watchdog" " exists at $rt_skill/scripts/watchdog.py"
    else
        fail "rebranding watchdog MISSING"
        all_ok=0
    fi

    # 6b: manifest exists
    if [ -f "$manifest" ]; then
        ok "rebranding manifest" " exists"
    else
        fail "rebranding manifest MISSING"
        all_ok=0
    fi

    # 6c: AatosTeam fork exists and is private
    local at_dir="$HOME/.hermes/tools/AatosTeam"
    if [ -d "$at_dir/.git" ]; then
        local is_private
        is_private=$(git -C "$at_dir" config --get remote.origin.url 2>/dev/null | grep -q "aatos-git-collab" && echo "yes" || echo "check_manually")
        ok "AatosTeam fork" " exists at $at_dir"
    else
        info "AatosTeam fork" " not at $at_dir"
    fi

    HEAL_STATUS+="rebranding-tools        $([ $all_ok -eq 1 ] && echo OK || echo FAIL)     watchdog ready\n"
}

heal_nginx_proxy_manager() {
    local all_ok=1
    local npm_skill="$HOME/.hermes/skills/nginx-proxy-manager"
    local heal_script="$npm_skill/scripts/self-heal.sh"
    local backup_script="$npm_skill/scripts/backup-npm.sh"
    local npm_container="npm"

    # 7a: Skill directory exists
    if [ -d "$npm_skill" ]; then
        ok "nginx-proxy-manager skill" " exists"
    else
        fail "nginx-proxy-manager skill MISSING"
        all_ok=0
    fi

    # 7b: NPM container running
    if docker ps --filter "name=$npm_container" --format "{{.Names}}" | grep -q "^$npm_container$"; then
        ok "NPM container" " running"
    else
        fail "NPM container NOT running"
        all_ok=0
    fi

    # 7c: NPM self-heal script
    if [ -f "$heal_script" ]; then
        ok "NPM self-heal script" " exists"
    else
        fail "NPM self-heal script MISSING"
        all_ok=0
    fi

    # 7d: NPM backup script
    if [ -f "$backup_script" ]; then
        ok "NPM backup script" " exists"
    else
        fail "NPM backup script MISSING"
        all_ok=0
    fi

    # 7e: NPM database exists
    if [ -f "/root/npm/data/database.sqlite" ]; then
        ok "NPM database" " exists"
    else
        fail "NPM database MISSING"
        all_ok=0
    fi

    # 7f: SSL certs present
    local cert_count
    cert_count=$(find /root/npm/letsencrypt/live -mindepth 1 -maxdepth 1 -type d 2>/dev/null | wc -l)
    if [ "$cert_count" -ge 6 ]; then
        ok "SSL certs" " $cert_count present"
    else
        fail "SSL certs" " only $cert_count (expected 6+)"
        all_ok=0
    fi

    # 7g: Nginx config syntax
    local nginx_test
    nginx_test=$(docker exec "$npm_container" nginx -t 2>&1 || true)
    if echo "$nginx_test" | grep -q "syntax is ok"; then
        ok "Nginx config" " syntax OK"
    else
        fail "Nginx config" " has errors"
        all_ok=0
    fi

    # 7h: Proxy host confs
    local conf_count
    conf_count=$(find /root/npm/data/nginx/proxy_host -name "*.conf" 2>/dev/null | wc -l)
    if [ "$conf_count" -ge 8 ]; then
        ok "Proxy host confs" " $conf_count present"
    else
        fail "Proxy host confs" " only $conf_count (expected 8)"
        all_ok=0
    fi

    # 7i: Cron watchdog
    local cron_line
    cron_line=$(crontab -l 2>/dev/null | grep "nginx-proxy-manager" || echo "")
    if [ -n "$cron_line" ]; then
        ok "NPM cron watchdog" " active"
    else
        info "NPM cron watchdog" " not set (will be added)"
        all_ok=0
    fi

    HEAL_STATUS+="nginx-proxy-manager    $([ $all_ok -eq 1 ] && echo OK || echo FAIL)     $conf_count hosts, $cert_count certs\n"
}

