#!/bin/bash
# master-heal.sh — Master self-healing script for all Hermes systems
# Runs health checks + auto-fixes across aatosteam, hermes-browser, git-safety, config-backup
# Usage: bash master-heal.sh [--fix] [--component=name]
#   --fix         Actually apply fixes (default is dry-run)
#   --component=  Filter: aatosteam|hermes-browser|git-safety|config|hermes-agent|rebranding|nginx-proxy-manager|all

set -euo pipefail

SKILL_DIR="$HOME/.hermes/skills/self-improvement/heal"
LOG_DIR="$SKILL_DIR/logs"
MANIFEST="$SKILL_DIR/manifests/heal-manifest.json"
LOG="$LOG_DIR/heal-$(date +%Y%m%d-%H%M%S).log"

DO_FIX=0
COMPONENT="all"
for arg in "$@"; do
    case $arg in
        --fix)    DO_FIX=1 ;;
        --component=*) COMPONENT="${arg#*=}" ;;
    esac
done

mkdir -p "$LOG_DIR"

log() { echo "[$(date '+%Y-%m-%d %H:%M:%S')] [$1] $2" | tee -a "$LOG"; }
ok()  { log "OK"   "$1"; }
fail(){ log "FAIL" "$1"; }
info(){ log "INFO" "$1"; }

fix() {
    local desc="$1"; shift
    if [ "$DO_FIX" -eq 1 ]; then
        log "FIX" "$desc — applying: $*"
        eval "$@" >>"$LOG" 2>&1 && ok "Fixed: $desc" || fail "Failed to fix: $desc"
    else
        log "WOULD FIX" "$desc — run with --fix to apply"
    fi
}

report() {
    local status="$1"; shift
    echo ""
    echo "=== HEAL REPORT ($(date '+%H:%M:%S')) ==="
    echo "Mode: $([ "$DO_FIX" -eq 1 ] && echo 'FIX' || echo 'DRY-RUN')"
    echo "Component: $COMPONENT"
    echo ""
    printf "%-30s %-10s %s\n" "Component" "Status" "Detail"
    printf "%-30s %-10s %s\n" "---------" "------" "-----"
    while read -r comp stat detail; do
        printf "%-30s %-10s %s\n" "$comp" "$stat" "$detail"
    done <<< "$HEAL_STATUS"
    echo ""
    if [ "$DO_FIX" -eq 1 ]; then
        cat >> "$MANIFEST" << EOF
{
  "timestamp": "$(date -u +%Y-%m-%dT%H:%M:%SZ)",
  "mode": "fix",
  "component": "$COMPONENT",
  "report": $(printf '%s\n' "$HEAL_STATUS" | jq -Rs '.' 2>/dev/null || printf '%s' "$HEAL_STATUS")
}
EOF
        log "INFO" "Manifest updated: $MANIFEST"
    fi
    echo "Full log: $LOG"
}

HEAL_STATUS=""

# ═══════════════════════════════════════════════════════════════════════════
# COMPONENT 1: aatosteam
# ═══════════════════════════════════════════════════════════════════════════
heal_aatosteam() {
    local all_ok=1

    # 1a: Binary present
    if command -v aatosteam &>/dev/null; then
        ok "aatosteam binary" " $(command -v aatosteam)"
    else
        fail "aatosteam binary MISSING"
        fix "Install aatosteam" "pip install aatosteam --break-system-packages --ignore-installed PyJWT"
        all_ok=0
    fi

    # 1b: Version
    local ver
    ver=$(aatosteam --version 2>/dev/null | awk '{print $2}' || echo "unknown")
    if [ "$ver" != "unknown" ]; then
        ok "aatosteam version" " v$ver"
    fi

    # 1c: Data dir
    local data_dir="$HOME/.aatosteam"
    if [ -d "$data_dir" ]; then
        ok "aatosteam data dir" " $data_dir"
    else
        fail "aatosteam data dir MISSING"
        fix "Create aatosteam data dir" "mkdir -p $data_dir"
        all_ok=0
    fi

    # 1d: Config file — ensure defaults for root environment
    local cfg="$data_dir/config.yaml"
    if [ ! -f "$cfg" ]; then
        fail "aatosteam config.yaml missing"
        fix "Create aatosteam config with root-safe defaults" \
            "mkdir -p $data_dir && cat > $cfg << 'CEOFIX'
# aatosteam root-safe config — auto-generated by master-heal.sh
spawn_defaults:
  backend: tmux
  command:
    - claude
  skip_permissions: true
board:
  refresh_interval: 5
CEOFIX"
        all_ok=0
    else
        ok "aatosteam config.yaml" " exists"
        # Check for critical settings
        if ! grep -q "skip_permissions" "$cfg" 2>/dev/null; then
            fail "aatosteam config.yaml" " missing skip_permissions — root spawn will fail"
            fix "Add skip_permissions to config.yaml" \
                "sed -i 's/spawn_defaults:/spawn_defaults:\n  skip_permissions: true/' $cfg"
            all_ok=0
        else
            ok "aatosteam skip_permissions" " configured"
        fi
    fi

    # 1e: Config health
    local health
    health=$(aatosteam config health 2>/dev/null | grep -E "Exists|Writable" | tr '\n' ' ' || true)
    if echo "$health" | grep -q "yes.*yes"; then
        ok "aatosteam config health" " $health"
    else
        fail "aatosteam config health" " $health"
        all_ok=0
    fi

    # 1f: Teams clean — no zombie sessions
    local zombie
    zombie=$(tmux list-sessions 2>/dev/null | grep -c "aatosteam-" || echo "0")
    if [ "$zombie" -gt 3 ]; then
        fail "Zombie tmux sessions detected" " $zombie sessions — possible leaked agents"
        fix "Clean zombie aatosteam sessions" "tmux list-sessions -F '#{session_name}' | grep '^aatosteam-' | xargs -r tmux kill-session -t"
        all_ok=0
    else
        ok "Zombie tmux sessions" " $zombie (OK)"
    fi

    # 1g: presets loaded
    local presets
    presets=$(aatosteam --json preset list 2>/dev/null | python3 -c "import sys,json; d=json.load(sys.stdin); print(len(d))" 2>/dev/null || echo "0")
    if [ "$presets" -gt 0 ]; then
        ok "aatosteam presets" " $presets loaded"
    else
        fail "aatosteam presets" " none loaded (should have 13+)"
        all_ok=0
    fi

    # 1h: Ensure minimax-global preset exists (critical for our setup)
    if aatosteam --json preset list 2>/dev/null | python3 -c "import sys,json; d=json.load(sys.stdin); exit(0 if 'minimax-global' in d else 1)" 2>/dev/null; then
        ok "minimax-global preset" " present"
    else
        fail "minimax-global preset" " MISSING"
        all_ok=0
    fi

    HEAL_STATUS+="aatosteam              OK     v$ver, $presets presets, $zombie tmux sessions\n"
}

