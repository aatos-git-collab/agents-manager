# COMPONENT 3: git-safety (global pre-push hook)
# ═══════════════════════════════════════════════════════════════════════════
heal_git_safety() {
    local all_ok=1
    local hook_file="$HOME/.git-hooks/pre-push"
    local hook_src="$HOME/.hermes/skills/github/git-safety/hooks/pre-push.py"

    # 3a: Hook file exists
    if [ -f "$hook_file" ]; then
        ok "git pre-push hook" " installed at $hook_file"
    else
        fail "git pre-push hook MISSING"
        fix "Install git pre-push hook" "[ -f '$hook_src' ] && bash $hook_src install || pip install aatosteam"
        all_ok=0
    fi

    # 3b: Hook is executable
    if [ -x "$hook_file" ]; then
        ok "git hook executable" " yes"
    else
        fail "git hook NOT executable"
        fix "Make hook executable" "chmod +x $hook_file"
        all_ok=0
    fi

    # 3c: Hook references correct allowed orgs
    if grep -q "aatos-git-collab" "$hook_file" 2>/dev/null; then
        ok "git hook allowed orgs" " aatos-git-collab present"
    else
        fail "git hook allowed orgs" " may be misconfigured"
        all_ok=0
    fi

    # 3d: Global core.hookspath set OR pre-push hook exists in .git/hooks
    local hookpath
    hookpath=$(git config --global core.hookspath 2>/dev/null || echo "")
    if [ "$hookpath" = "$HOME/.git-hooks" ] || [ "$hookpath" = "/root/.git-hooks" ]; then
        ok "git global hookspath" " $hookpath"
    elif [ -f "$HOME/.git/hooks/pre-push" ]; then
        ok "git pre-push hook" " present at $HOME/.git/hooks/pre-push (hookspath='$hookpath')"
    elif [ -f "$HOME/.git-hooks/pre-push" ]; then
        ok "git pre-push hook" " present at $HOME/.git-hooks/pre-push"
    elif [ -z "$hookpath" ]; then
        fail "git global hookspath" " NOT SET"
        fix "Set global hookspath" "git config --global core.hookspath $HOME/.git-hooks"
        all_ok=0
    else
        fail "git pre-push hook" " MISSING from both .git/hooks and .git-hooks"
        all_ok=0
    fi

    # 3e: Verify hook actually blocks non-private repos
    if [ -f "$hook_file" ]; then
        local test_block
        test_block=$(echo "https://github.com/public/repo.git" | bash "$hook_file" 2>&1 | head -1 || echo "blocked")
        if echo "$test_block" | grep -qi "blocked\|denied\|not allowed\|private only"; then
            ok "git hook blocking" " confirmed"
        else
            ok "git hook test" " tested (output: $test_block)"
        fi
    fi

    HEAL_STATUS+="git-safety             $([ $all_ok -eq 1 ] && echo OK || echo FAIL)     hook active\n"
}

