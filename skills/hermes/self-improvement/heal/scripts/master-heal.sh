# COMPONENT 2: hermes-browser + stealth-overrides
# ═══════════════════════════════════════════════════════════════════════════
heal_hermes_browser() {
    local all_ok=1
    local browser_skill="$HOME/.hermes/skills/hermes-browser"
    local stealth_src="$browser_skill/src/stealth-overrides.js"

    # 2a: stealth-overrides.js present
    if [ -f "$stealth_src" ]; then
        ok "stealth-overrides.js" " exists"
    else
        fail "stealth-overrides.js MISSING"
        fix "Restore stealth-overrides.js" \
            "cat > $stealth_src << 'JSEOF'
// stealth-overrides.js — Camoufox OS fingerprint patch
// Patches navigator.platform, oscpu, hardwareConcurrency, deviceMemory
(function() {
  const overrides = {
    navigator: {
      platform: 'Win32',
      oscpu: 'Windows NT 10.0',
      hardwareConcurrency: 8,
      deviceMemory: 8
    }
  };
  Object.entries(overrides).forEach(([obj, props]) => {
    const target = obj === 'navigator' ? window.navigator : window[obj];
    if (target) Object.defineProperties(target, 
      Object.fromEntries(Object.entries(props).map(([k, v]) => 
        [k, { value: v, writable: true, configurable: true }]
      ))
    );
  });
})();
JSEOF"
        all_ok=0
    fi

    # 2b: stealth-overrides.js patched in node_modules
    local nm_override="$HOME/.hermes/hermes-agent/node_modules/@askjo/camoufox-browser/server/static/js/stealth-overrides.js"
    if [ -f "$stealth_src" ] && [ -f "$nm_override" ]; then
        if diff -q "$stealth_src" "$nm_override" &>/dev/null; then
            ok "stealth-overrides.js in node_modules" " matches"
        else
            fail "stealth-overrides.js in node_modules" " DIFFERS from skill source"
            fix "Patch node_modules stealth-overrides.js" "cp $stealth_src $nm_override"
            all_ok=0
        fi
    elif [ -f "$stealth_src" ]; then
        info "node_modules override" " skill src exists, nm file not checked"
    fi

    # 2c: camoufox service health
    local health
    health=$(curl -s --max-time 5 http://localhost:9377/health 2>/dev/null || echo '{"ok":false}')
    if echo "$health" | grep -q '"ok":true'; then
        local engine=$(echo "$health" | python3 -c "import sys,json; print(json.load(sys.stdin).get('engine','?'))" 2>/dev/null || echo "?")
        ok "camoufox health" " engine=$engine port=9377"
    else
        fail "camoufox health" " UNHEALTHY or not running"
        fix "Run hermes-browser self-heal" "bash $browser_skill/run.sh heal" 2>/dev/null || \
        fix "Start camoufox manually" \
            "cd $HOME/.hermes/hermes-agent/node_modules/@askjo/camoufox-browser && PORT=9377 nohup node server.js > $browser_skill/camofox.log 2>&1 &"
        all_ok=0
    fi

    # 2d: camofox log not overflowing
    local logfile="$browser_skill/camofox.log"
    if [ -f "$logfile" ]; then
        local logsize
        logsize=$(wc -c < "$logfile" 2>/dev/null || echo 0)
        if [ "$logsize" -gt 52428800 ]; then
            fail "camofox.log size" " $((logsize/1024/1024))MB — too large"
            fix "Truncate camofox.log" "> $logfile"
            all_ok=0
        else
            ok "camofox.log size" " $((logsize/1024))KB"
        fi
    fi

    # 2e: managed persistence dir
    local persist="$HOME/.hermes/browser_auth/camofox"
    if [ -d "$persist" ]; then
        ok "browser_auth dir" " $persist"
    else
        info "browser_auth dir" " not present (first run will create)"
    fi

    HEAL_STATUS+="hermes-browser         $([ $all_ok -eq 1 ] && echo OK || echo FAIL)     camoufox port=9377\n"
}

