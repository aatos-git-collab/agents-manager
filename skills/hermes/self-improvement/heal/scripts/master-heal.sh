# COMPONENT 4: config-backup-restore (manifest + cron watchdog)
# ═══════════════════════════════════════════════════════════════════════════
heal_config_backup() {
    local all_ok=1
    local cbr_skill="$HOME/.hermes/skills/config-backup-restore"
    local manifest="$cbr_skill/manifests/backup-manifest.json"
    local self_heal="$cbr_skill/scripts/self-heal.sh"

    # 4a: Skill directory
    if [ -d "$cbr_skill" ]; then
        ok "config-backup-restore skill" " exists"
    else
        fail "config-backup-restore skill MISSING"
        fix "Create config-backup-restore skill" \
            "mkdir -p $cbr_skill/scripts $cbr_skill/manifests $cbr_skill/logs"
        all_ok=0
    fi

    # 4b: self-heal.sh exists and executable
    if [ -f "$self_heal" ] && [ -x "$self_heal" ]; then
        ok "self-heal.sh" " present and executable"
    elif [ -f "$self_heal" ]; then
        fail "self-heal.sh not executable"
        fix "Make self-heal.sh executable" "chmod +x $self_heal"
        all_ok=0
    else
        fail "self-heal.sh MISSING"
        all_ok=0
    fi

    # 4c: Hermes cron system running (jobs managed by Hermes, not system crontab)
    # Hermes cron jobs listed by job_id in ~/.hermes/crons/ or via cron tool API
    # Verify by checking if hermes cron dir exists and has our job
    local hermes_cron_dir="$HOME/.hermes/cron"
    if [ -d "$hermes_cron_dir" ]; then
        local job_count
        job_count=$(ls "$hermes_cron_dir"/*.json 2>/dev/null | wc -l || echo "0")
        ok "hermes cron system" " active, $job_count jobs tracked"
    elif pgrep -f "hermes.*cron\|cron.*scheduler" &>/dev/null; then
        ok "hermes cron" " process running"
    elif pgrep -f "cron" &>/dev/null; then
        ok "system cron daemon" " running (hermes jobs managed internally)"
    else
        fail "cron system" " neither hermes cron nor system cron confirmed"
        all_ok=0
    fi

    # 4d: Backup clone up-to-date
    local backup_clone="/tmp/agents-backup-restore"
    if [ -d "$backup_clone/.git" ]; then
        local age_sec
        age_sec=$(($(date +%s) - $(stat -c %Y "$backup_clone" 2>/dev/null || stat -f %m "$backup_clone" 2>/dev/null || echo 0)))
        if [ "$age_sec" -gt 86400 ]; then
            fail "Backup clone stale" " $((age_sec/3600))h old (>24h)"
            fix "Refresh backup clone" "git -C $backup_clone pull origin chro"
            all_ok=0
        else
            ok "Backup clone fresh" " $((age_sec/3600))h old"
        fi
    else
        fail "Backup clone MISSING"
        fix "Clone backup repo" "git clone --branch chro --single-branch https://github.com/aatos-git-collab/agents-backup.git $backup_clone"
        all_ok=0
    fi

    # 4e: Manifest readable
    if [ -f "$manifest" ]; then
        local last_heal
        last_heal=$(python3 -c "import json; d=json.load(open('$manifest')); print(d.get('last_self_heal','never'))" 2>/dev/null || echo "unreadable")
        ok "heal manifest" " last=$last_heal"
    else
        info "heal manifest" " not yet created"
    fi

    HEAL_STATUS+="config-backup-restore  $([ $all_ok -eq 1 ] && echo OK || echo FAIL)     watchdog active\n"
}

# ═══════════════════════════════════════════════════════════════════════════
# COMPONENT 5: hermes-agent health
# ═══════════════════════════════════════════════════════════════════════════
heal_hermes_agent() {
    local all_ok=1

    # 5a: hermes binary (hermes-agent CLI)
    local ha_bin
    ha_bin=$(command -v hermes 2>/dev/null || command -v hermes-agent 2>/dev/null || echo "")
    if [ -n "$ha_bin" ]; then
        ok "hermes binary" " $ha_bin"
    else
        fail "hermes binary MISSING"
        all_ok=0
    fi

    # 5b: gateway running
    if [ -f "$HOME/.hermes/gateway.pid" ] || pgrep -f "hermes-gateway\|gateway.js" &>/dev/null; then
        ok "hermes gateway" " running"
    else
        info "hermes gateway" " not checked (may be systemd-managed)"
    fi

    # 5c: config.yaml version
    local cfg_ver
    cfg_ver=$(grep '_config_version:' "$HOME/.hermes/config.yaml" 2>/dev/null | awk '{print $2}' || echo "unknown")
    if [ "$cfg_ver" != "unknown" ]; then
        ok "config.yaml version" " v$cfg_ver"
    else
        fail "config.yaml version" " UNKNOWN"
        all_ok=0
    fi

    # 5d: Skills directory count
    local skill_count
    skill_count=$(ls -1 "$HOME/.hermes/skills/" 2>/dev/null | wc -l)
    if [ "$skill_count" -gt 40 ]; then
        ok "hermes skills count" " $skill_count (healthy)"
    else
        fail "hermes skills count" " only $skill_count (expected 40+)"
        all_ok=0
    fi

    # 5e: Memory files
    if [ -f "$HOME/.hermes/SOUL.md" ]; then
        ok "SOUL.md" " present"
    else
        fail "SOUL.md MISSING"
        all_ok=0
    fi

    if [ -f "$HOME/.hermes/USER.md" ]; then
        ok "USER.md" " present"
    else
        fail "USER.md MISSING"
        all_ok=0
    fi

    HEAL_STATUS+="hermes-agent           $([ $all_ok -eq 1 ] && echo OK || echo FAIL)     v$cfg_ver, $skill_count skills\n"
}

